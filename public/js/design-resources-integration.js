    // Get resource statistics
    getResourceStatistics() {
        const stats = {};
        
        Object.entries(this.resources).forEach(([category, resources]) => {
            stats[category] = {
                subcategories: Object.keys(resources).length,
                totalResources: Object.values(resources).reduce((total, subcategory) => total + subcategory.length, 0)
            };
        });
        
        return stats;
    }

    // Get resource by URL
    getResourceByURL(url) {
        return this.searchIndex.find(item => item.url === url);
    }

    // Get related resources
    getRelatedResources(resource) {
        const sameCategory = this.searchIndex.filter(item => 
            item.category === resource.category && item.url !== resource.url
        );
        
        return sameCategory.slice(0, 5);
    }

