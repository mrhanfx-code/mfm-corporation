    // Initialize the integration
    initializeIntegration() {
        this.setupAPI();
        this.initializeResourceLibrary();
        this.configureSearch();
        return this.getIntegrationStatus();
    }

    // Build search index
    buildSearchIndex() {
        const index = [];
        
        Object.entries(this.resources).forEach(([category, resources]) => {
            Object.entries(resources).forEach(([subcategory, items]) => {
                items.forEach(item => {
                    index.push({
                        category,
                        subcategory,
                        name: item.name,
                        url: item.url,
                        description: item.description,
                        searchable: `${item.name} ${item.description} ${category} ${subcategory}`.toLowerCase()
                    });
                });
            });
        });
        
        return index;
    }

    // Setup API
    setupAPI() {
        window.MFMDesignResources = {
            search: (query) => this.searchResources(query),
            getCategory: (category) => this.getCategory(category),
            getSubcategory: (category, subcategory) => this.getSubcategory(category, subcategory),
            getAllCategories: () => this.categories,
            getRandomResource: (category) => this.getRandomResource(category),
            getPopularResources: () => this.getPopularResources(),
            getRecentResources: () => this.getRecentResources()
        };
    }

    // Initialize resource library
    initializeResourceLibrary() {
        this.resourceLibrary = {
            totalResources: this.searchIndex.length,
            categories: this.categories.length,
            lastUpdate: new Date().toISOString(),
            featured: this.getFeaturedResources()
        };
    }

    // Configure search
    configureSearch() {
        this.searchConfig = {
            minQueryLength: 2,
            maxResults: 20,
            fuzzySearch: true,
            categories: this.categories
        };
    }

    // Search resources
    searchResources(query) {
        if (!query || query.length < this.searchConfig.minQueryLength) {
            return [];
        }
        
        const normalizedQuery = query.toLowerCase();
        const results = this.searchIndex
            .filter(item => item.searchable.includes(normalizedQuery))
            .slice(0, this.searchConfig.maxResults);
        
        return results;
    }

    // Get category resources
    getCategory(category) {
        return this.resources[category] || {};
    }

    // Get subcategory resources
    getSubcategory(category, subcategory) {
        const categoryResources = this.getCategory(category);
        return categoryResources[subcategory] || [];
    }

    // Get random resource
    getRandomResource(category) {
        const categoryResources = this.getCategory(category);
        const allResources = [];
        
        Object.values(categoryResources).forEach(subcategory => {
            allResources.push(...subcategory);
        });
        
        if (allResources.length === 0) {
            return null;
        }
        
        const randomIndex = Math.floor(Math.random() * allResources.length);
        return allResources[randomIndex];
    }

